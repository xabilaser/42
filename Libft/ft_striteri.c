void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int	i;

	if (!s || !f)
		return ;
	i = -1;
	while (s[i + 1] != '\0')
	{
		i++;
	}
	while (i >= 0)
	{
		(*f)(i, &s[i]);
		i--;
	}
}
